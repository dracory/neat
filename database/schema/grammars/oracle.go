func (r *Oracle) CompileCreate(blueprint schema.Blueprint) (string, error) {
	columns, err := r.getColumns(blueprint)
	if err != nil {
		return "", err
	}
	primaryCommand := getCommandByName(blueprint.GetCommands(), "primary")
	if primaryCommand != nil {
		// Check if the primary key is on a single identity column
		// If so, add it inline to avoid Oracle syntax issues
		if len(primaryCommand.Columns) == 1 {
			for i, col := range columns {
				// Check if this column is the primary key column and has identity
				if strings.Contains(col, "generated by default as identity") {
					for _, pkCol := range primaryCommand.Columns {
						quotedPkCol, err := r.wrap.Column(pkCol)
						if err != nil {
							return "", err
						}
						if strings.HasPrefix(col, quotedPkCol+" ") {
							// Add PRIMARY KEY inline
							columns[i] = col + " primary key"
							primaryCommand.ShouldBeSkipped = true
							goto done
						}
					}
				}
			}
		}
		// If not handled inline, add as separate constraint
		if !primaryCommand.ShouldBeSkipped {
			columnized, err := r.wrap.Columnize(primaryCommand.Columns)
			if err != nil {
				return "", err
			}
			columns = append(columns, fmt.Sprintf("primary key (%s)", columnized))
			primaryCommand.ShouldBeSkipped = true
		}
	}

done:
	table, err := r.wrap.Table(blueprint.GetTableName())
	if err != nil {
		return "", err
	}
	return fmt.Sprintf("create table %s (%s)", table, strings.Join(columns, ", ")), nil
}

